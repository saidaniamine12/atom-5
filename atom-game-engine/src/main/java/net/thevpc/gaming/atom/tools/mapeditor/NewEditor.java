    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser c = new JFileChooser();
        c.setFileSelectionMode(JFileChooser.FILES_ONLY);
        c.addChoosableFileFilter(new FileNameExtensionFilter("map project file", "mpj"));
        c.setSelectedFile(null);
        int r = c.showSaveDialog(null);
        if (r == JFileChooser.APPROVE_OPTION) {
            try {
                projectFileField.setText(c.getSelectedFile().getCanonicalPath());
            } catch (IOException ex) {
                projectFileField.setText(c.getSelectedFile().getAbsolutePath());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

